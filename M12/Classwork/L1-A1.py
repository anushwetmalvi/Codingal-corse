num1 = 10
num2 = 5

print("num1 & num2 is-", num1, num2)
print("num1 & num2 are-", num1 & num2)  # AND operator
print("num1 | num2 are-", num1 | num2)  # OR operator
print("num1 ^ num2 are-", num1 ^ num2)  # XOR operator
print("num1 << 2 is-", num1 << num2)    # left shift operator
print("num2 >> 2 is-", num1 >> num2)    # right shift
print("~num1 is-", ~num1)               # negation operator
print("~num2 is-", ~num2)               # negation operator



